package net.easyunion.common.vo;

import java.util.List;
import java.util.Map;

/**
 * 返回结果
 * @author lipenghui
 *
 * @version 创建时间：2013年11月26日  上午11:52:15
 */
public class ResultVo {

	private long messageId;
	private boolean result;
	private List<Map<String,Object>> resultObj;
	private int count;
	private String date;
    private String errMsg;


    public String getErrMsg() {
        return errMsg;
    }

    public void setErrMsg(String errMsg) {
        this.errMsg = errMsg;
    }
	public long getMessageId() {
		return messageId;
	}
	public void setMessageId(long messageId) {
		this.messageId = messageId;
	}
	public boolean isResult() {
		return result;
	}
	public void setResult(boolean result) {
		this.result = result;
	}
	public List<Map<String,Object>> getResultObj() {
		return resultObj;
	}
	public void setResultObj(List<Map<String,Object>> resultObj) {
		this.resultObj = resultObj;
	}
	public int getCount() {
		return count;
	}
	public void setCount(int count) {
		this.count = count;
	}
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	
	
}
